package cn.edu.sau.pojo;

public class House {
    private String username;        // 房主姓名
    private String phone;           // 房主联系方式
    private String rent;            // 房屋租金
    private String apartment;       // 房屋户型
    private String area;            // 房屋面积
    private String flr;             // 房屋楼层
    private String orientation;     // 房屋朝向
    private String condi;           // 设施条件
    private String residential_quarters;   // 所在小区
    private String respective_area; // 所属区域
    private String address;         // 具体地址
    private String property_right;  // 房屋产权
    private String description;     // 房屋描述
    private String image1;          // 房屋照片
    private String image2;
    private String image3;
    private String stater;          // 房屋状态
    private int owner_id;           // 房主id

    public String getUsername() {
        return username;
    }
    public void setUsername(String username) {
        this.username = username;
    }
    public String getPhone() {
        return phone;
    }
    public void setPhone(String phone) {
        this.phone = phone;
    }
    public String getRent() {
        return rent;
    }
    public void setRent(String rent) {
        this.rent = rent;
    }
    public String getApartment() {
        return apartment;
    }
    public void setApartment(String apartment) {
        this.apartment = apartment;
    }
    public String getArea() {
        return area;
    }
    public void setArea(String area) {
        this.area = area;
    }
    public String getFlr() {
        return flr;
    }
    public void setFlr(String flr) {
        this.flr = flr;
    }
    public String getOrientation() {
        return orientation;
    }
    public void setOrientation(String orientation) {
        this.orientation = orientation;
    }
    public String getCondi() {
        return condi;
    }
    public void setCondi(String condi) {
        this.condi = condi;
    }
    public String getResidential_quarters() {
        return residential_quarters;
    }
    public void setResidential_quarters(String residential_quarters) {
        this.residential_quarters = residential_quarters;
    }
    public String getRespective_area() {
        return respective_area;
    }
    public void setRespective_area(String respective_area) {
        this.respective_area = respective_area;
    }
    public String getAddress() {
        return address;
    }
    public void setAddress(String address) {
        this.address = address;
    }
    public String getProperty_right() {
        return property_right;
    }
    public void setProperty_right(String property_right) {
        this.property_right = property_right;
    }
    public String getDescription() {
        return description;
    }
    public void setDescription(String description) {
        this.description = description;
    }
    public String getImage1() {
        return image1;
    }
    public void setImage1(String image1) {
        this.image1 = image1;
    }
    public String getImage2() {
        return image2;
    }
    public void setImage2(String image2) {
        this.image2 = image2;
    }
    public String getImage3() {
        return image3;
    }
    public void setImage3(String image3) {
        this.image3 = image3;
    }
    public String getStater() {
        return stater;
    }
    public void setStater(String stater) {
        this.stater = stater;
    }
    public int getOwner_id() {
        return owner_id;
    }
    public void setOwner_id(int owner_id) {
        this.owner_id = owner_id;
    }
}
